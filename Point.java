public class Point {

    public int x;
    public int y;
    public int predX;
    public int predY;

    public Point() {
        this.x = 0;
        this.y = 0;
        this.predX = 0;
        this.predY = 0;

    }

    public Point(int x, int y) {
        this.x = x;
        this.y = y;
        this.predX = 0;
        this.predY = 0;

    }

    
}